            rhs[1]=-DN(0,0)*stress[3] + DN(0,1)*crhs0 - DN(0,1)*stress[1] - DN(0,2)*stress[4] + N[0]*crhs15 - N[0]*crhs20;
            rhs[2]=-DN(0,0)*stress[5] - DN(0,1)*stress[4] + DN(0,2)*crhs0 - DN(0,2)*stress[2] + N[0]*crhs21 - N[0]*crhs26;
