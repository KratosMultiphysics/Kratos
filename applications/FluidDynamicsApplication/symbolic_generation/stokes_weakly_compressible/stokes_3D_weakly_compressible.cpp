            rhs[5]=-DN(1,0)*stress[3] + DN(1,1)*crhs0 - DN(1,1)*stress[1] - DN(1,2)*stress[4] + N[1]*crhs15 - N[1]*crhs20;
            rhs[6]=-DN(1,0)*stress[5] - DN(1,1)*stress[4] + DN(1,2)*crhs0 - DN(1,2)*stress[2] + N[1]*crhs21 - N[1]*crhs26;
            rhs[7]=-N[1]*crhs27;
            rhs[8]=DN(2,0)*crhs0 - DN(2,0)*stress[0] - DN(2,1)*stress[3] - DN(2,2)*stress[5] + N[2]*crhs1 - N[2]*crhs14;
            rhs[9]=-DN(2,0)*stress[3] + DN(2,1)*crhs0 - DN(2,1)*stress[1] - DN(2,2)*stress[4] + N[2]*crhs15 - N[2]*crhs20;
            rhs[10]=-DN(2,0)*stress[5] - DN(2,1)*stress[4] + DN(2,2)*crhs0 - DN(2,2)*stress[2] + N[2]*crhs21 - N[2]*crhs26;
            rhs[11]=-N[2]*crhs27;
            rhs[12]=DN(3,0)*crhs0 - DN(3,0)*stress[0] - DN(3,1)*stress[3] - DN(3,2)*stress[5] + N[3]*crhs1 - N[3]*crhs14;
            rhs[13]=-DN(3,0)*stress[3] + DN(3,1)*crhs0 - DN(3,1)*stress[1] - DN(3,2)*stress[4] + N[3]*crhs15 - N[3]*crhs20;
            rhs[14]=-DN(3,0)*stress[5] - DN(3,1)*stress[4] + DN(3,2)*crhs0 - DN(3,2)*stress[2] + N[3]*crhs21 - N[3]*crhs26;
