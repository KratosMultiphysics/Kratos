// MED files are plain HDF5 files following a documented, terse group/dataset layout.
// Rather than linking against the "med" C library (a thin, MPI-aware wrapper around
// HDF5), this file talks to that layout directly through the HDF5 C API. The layout
// below was confirmed empirically with "h5dump" against real MED files (produced by
// Salome / libmed) and cross-checked against the libmed source (src/hdfi, src/ci).
//
//   /INFOS_GENERALES                                     attrs: MAJ, MIN, REL
//   /ENS_MAA/<mesh>                                      attrs: DIM, ESP, DES, NOM, UNI, UNT, NXI, NXT, REP, SRT, TYP
//   /ENS_MAA/<mesh>/<step>                                attrs: CGT, NDT, NOR, NXI, NXT, PDT, PVI, PVT
//   /ENS_MAA/<mesh>/<step>/NOE/COO                        dataset, float64, len = num_nodes * 3
//   /ENS_MAA/<mesh>/<step>/NOE/FAM                        dataset, int64,   len = num_nodes (optional)
//   /ENS_MAA/<mesh>/<step>/NOE/NUM                        dataset, int64,   len = num_nodes (optional)
//   /ENS_MAA/<mesh>/<step>/MAI/<geom_code>/NOD            dataset, int64,   len = num_entities * nodes_per_entity, 1-based
//   /ENS_MAA/<mesh>/<step>/MAI/<geom_code>/FAM            dataset, int64,   len = num_entities (optional)
//   /ENS_MAA/<mesh>/<step>/MAI/<geom_code>/NUM            dataset, int64,   len = num_entities (optional)
//   /FAS/<mesh>/ELEME/<family_group_name>                 attr: NUM (family number)
//   /FAS/<mesh>/ELEME/<family_group_name>/GRO/NOM         dataset, fixed-width 80-byte names, one row per group
//   /FAS/<mesh>/NOEUD/<family_group_name>                 same, for node families

constexpr const char* MedGroupMeshes = "ENS_MAA";
constexpr const char* MedGroupFamilies = "FAS";
constexpr const char* MedGroupGeometries = "MAI";
constexpr const char* MedGroupNodes = "NOE";
constexpr const char* MedDatasetCoordinates = "COO";
constexpr const char* MedDatasetConnectivity = "NOD";
constexpr const char* MedDatasetFamily = "FAM";
constexpr const char* MedDatasetGlobalNumber = "NUM";
constexpr const char* MedGroupElementFamilies = "ELEME";
constexpr const char* MedGroupNodeFamilies = "NOEUD";
constexpr const char* MedGroupVersionInfo = "INFOS_GENERALES";
constexpr const char* MedGroupGroupNames = "GRO";
constexpr const char* MedDatasetGroupNames = "NOM";

// MED's fixed with for group (SubModelPart) names, historically called "MED_LNAME_SIZE".
constexpr hsize_t MedGroupNameSize = 80;

// The single computation-step name MED uses for MED_NO_DT/MED_NO_IT (the only step
// Kratos ever writes), as generated by libmed's "_MEDgetComputationStepName" (20-digit,
// zero-padded, sign-prefixed encoding of numdt and numit, both -1 here).
constexpr const char* MedSingleStepName = "-0000000000000000001-0000000000000000001";

