void CheckConnectivitiesSize(
    const std::size_t ExpectedSize,
    const std::vector<int64_t>& rConnectivities)
{
    KRATOS_DEBUG_ERROR_IF_NOT(rConnectivities.size() == ExpectedSize)
        << "Connectivities must have a size of " << ExpectedSize << ", but have " << rConnectivities.size() << "!" << std::endl;
}

// Converts connectivities between MED's and Kratos' node ordering (in-place). The
// math here is unchanged from the previous med-library-based implementation; only
// the lookup key changed from libmed's numeric "med_geometry_type" to the MED short
// geometry code string (now obtained directly as an HDF5 group name).
std::function<void(std::vector<int64_t>&)> GetReorderFunction(const std::string& rMedGeomCode)
{
    if (rMedGeomCode == "TR3") {
        return [](std::vector<int64_t>& rConn){
            CheckConnectivitiesSize(3, rConn);
            std::swap(rConn[1], rConn[2]);
        };
    } else if (rMedGeomCode == "TR6") {
        return [](std::vector<int64_t>& rConn){
            CheckConnectivitiesSize(6, rConn);
            std::swap(rConn[1], rConn[2]);
            std::swap(rConn[3], rConn[5]);
        };
    } else if (rMedGeomCode == "QU4") {
        return [](std::vector<int64_t>& rConn){
            CheckConnectivitiesSize(4, rConn);
            std::swap(rConn[1], rConn[3]);
        };
    } else if (rMedGeomCode == "QU8") {
        return [](std::vector<int64_t>& rConn){
            CheckConnectivitiesSize(8, rConn);
            std::swap(rConn[1], rConn[3]);
            std::swap(rConn[4], rConn[7]);
            std::swap(rConn[5], rConn[6]);
        };
    } else if (rMedGeomCode == "QU9") {
        return [](std::vector<int64_t>& rConn){
            CheckConnectivitiesSize(9, rConn);
            std::swap(rConn[1], rConn[3]);
            std::swap(rConn[4], rConn[7]);
            std::swap(rConn[5], rConn[6]);
        };
    } else if (rMedGeomCode == "TE4") {
        return [](std::vector<int64_t>& rConn){
            CheckConnectivitiesSize(4, rConn);
            std::swap(rConn[2], rConn[3]);
        };
    } else if (rMedGeomCode == "T10") {
        return [](std::vector<int64_t>& rConn){
            CheckConnectivitiesSize(10, rConn);
            std::swap(rConn[1], rConn[2]);
            std::swap(rConn[4], rConn[6]);
            std::swap(rConn[8], rConn[9]);
        };
    } else if (rMedGeomCode == "HE8") {
        return [](std::vector<int64_t>& rConn){
            CheckConnectivitiesSize(8, rConn);
            std::swap(rConn[1], rConn[4]);
            std::swap(rConn[2], rConn[7]);
        };
    } else if (rMedGeomCode == "H20" || rMedGeomCode == "H27" || rMedGeomCode == "PY5" ||
               rMedGeomCode == "P13" || rMedGeomCode == "PE6" || rMedGeomCode == "P15") {
        KRATOS_ERROR << "Reordering for MED geometry type \"" << rMedGeomCode << "\" is not implemented!" << std::endl;
    }

    // no reordering required (PO1, SE2, SE3)
    return [](std::vector<int64_t>&){};
}

// Replaces the previous "GetKratosGeometryName" switch (keyed by libmed's numeric
// "med_geometry_type") with a table keyed by the MED short geometry code ("TE4",
// "HE8", ...), since that code is now obtained directly as the HDF5 group name
// under "MAI" rather than from libmed's numeric encoding.
struct MedGeometryInfo
{
    std::size_t NumNodes;
    std::string Kratos3DName;
    std::string Kratos2DName; // empty if this type has no 2D-in-2D-space variant
};

const std::map<std::string, MedGeometryInfo>& GetMedGeometryTable()
{
    static const std::map<std::string, MedGeometryInfo> table {
        { "PO1", { 1,  "Point3D",          "Point2D" } },
        { "SE2", { 2,  "Line3D2",          "Line2D2" } },
        { "SE3", { 3,  "Line3D3",          "Line2D3" } },
        { "TR3", { 3,  "Triangle3D3",      "Triangle2D3" } },
        { "TR6", { 6,  "Triangle3D6",      "Triangle2D6" } },
        { "QU4", { 4,  "Quadrilateral3D4", "Quadrilateral2D4" } },
        { "QU8", { 8,  "Quadrilateral3D8", "Quadrilateral2D8" } },
        { "QU9", { 9,  "Quadrilateral3D9", "Quadrilateral2D9" } },
        { "TE4", { 4,  "Tetrahedra3D4",    "" } },
        { "T10", { 10, "Tetrahedra3D10",   "" } },
        { "PY5", { 5,  "Pyramid3D5",       "" } },
        { "P13", { 13, "Pyramid3D13",      "" } },
        { "PE6", { 6,  "Prism3D6",         "" } },
        { "P15", { 15, "Prism3D15",        "" } },
        { "HE8", { 8,  "Hexahedra3D8",     "" } },
        { "H20", { 20, "Hexahedra3D20",    "" } },
        { "H27", { 27, "Hexahedra3D27",    "" } }
    };
    return table;
}

const MedGeometryInfo& GetMedGeometryInfo(const std::string& rMedGeomCode)
{
    const auto& r_table = GetMedGeometryTable();
    const auto it = r_table.find(rMedGeomCode);
    KRATOS_ERROR_IF(it == r_table.end()) << "MED geometry type \"" << rMedGeomCode << "\" is not available!" << std::endl;
    return it->second;
}

std::string GetKratosGeometryName(
    const std::string& rMedGeomCode,
    const int Dimension)
{
    const auto& r_info = GetMedGeometryInfo(rMedGeomCode);
    if (Dimension == 2 && !r_info.Kratos2DName.empty()) {
        return r_info.Kratos2DName;
    }
    return r_info.Kratos3DName;
}

// =========================================================================
// SubModelPart hierarchy helper (unchanged from the med-library-based version)
// =========================================================================

ModelPart& GetOrCreateSubModelPartHierarchical(ModelPart& rRoot,
                                            const std::string& rFullName,
                                            std::unordered_map<std::string, ModelPart*>& rCache)
{
    auto it_cached = rCache.find(rFullName);
    if (it_cached != rCache.end()) {
        return *(it_cached->second);
    }

    std::stringstream ss(rFullName);
    std::string token;

    ModelPart* p_current = &rRoot;
    std::string current_path;

    while (std::getline(ss, token, '.')) {
        if (!current_path.empty()) current_path += ".";
        current_path += token;

        auto it = rCache.find(current_path);
        if (it != rCache.end()) {
            p_current = it->second;
            continue;
        }

        if (!p_current->HasSubModelPart(token)) {
            p_current = &p_current->CreateSubModelPart(token);
        } else {
            p_current = &p_current->GetSubModelPart(token);
        }

        rCache[current_path] = p_current;
    }

    return *p_current;
}

